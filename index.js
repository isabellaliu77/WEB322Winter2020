


/*Template literals are string literals allowing embedded expressions.
Template literals are enclosed by the back-tick (` `)instead of double or single quotes. 
Template literals can contain placeholders. 
These are indicated by the dollar sign and curly braces (${expression}). 
*/
//Example
let name="Jon Snow";
let age=25;
console.log(`Welcome,${name}, you don't look like a day over ${age-5}`);
